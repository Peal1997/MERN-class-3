
const body = document.body
const labels = document.querySelectorAll('label')
const form = document.querySelector('form')
const msg = document.querySelector('.msg')
const p = document.querySelector('p')


labels.forEach(label => {
     label.firstElementChild.onchange = () => {
        if(label.firstElementChild.value == 'dark'){
            body.style.backgroundColor = '#191923'
            labels.forEach(item => {
                item.style.color = '#fff'
            })
        }else{
            body.style.backgroundColor = '#fff'
            labels.forEach(item => {
                item.style.color = 'black'
            })
        }
     }
})

form.onsubmit = (event) => {
    event.preventDefault()
    const form_data = new FormData(event.target)
    const {name , email , cell} = Object.fromEntries(form_data)
    if(!name || !email || !cell){
        msg.innerHTML = alert('warning' , 'All fields are required')
    }else{
        msg.innerHTML = alert('success' , 'Data submited successful')
        p.innerHTML = `${name} , ${email} , ${cell}`

    }
     
}




































































































































































































































































































